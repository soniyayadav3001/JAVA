public class Main {
  public static void main(String[] args) {
    // create double variables
    double a = 23.78;
    double b = 52.11;

    // convert double into int
    // using typecasting
    int c = (int)a;
    int d = (int)b;

    System.out.println(c);     // 23
    System.out.println(d);     // 52
  }
}
